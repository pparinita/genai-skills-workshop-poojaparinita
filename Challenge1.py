def get_nws_weather(latitude: float, longitude: float) -> Optional[Dict]:
    """
    Retrieves current weather forecast data from the National Weather Service (NWS) API
    for a given latitude and longitude.

    This function first queries the NWS /points endpoint to get the forecast office
    and grid coordinates, then uses that information to fetch the detailed forecast
    from the /gridpoints endpoint.

    Args:
        latitude: The geographical latitude (e.g., 39.7456).
        longitude: The geographical longitude (e.6., -97.6608).

    Returns:
        A dictionary containing the weather forecast data if successful,
        otherwise None.
    """
    base_url = "https://api.weather.gov"
    headers = {
        "User-Agent": "Google Colab NWS Weather App (student-04-cd059360d159@qwiklabs.net)",
        "Accept": "application/ld+json" # Or application/geo+json
    }

    # Step 1: Get forecast office and grid coordinates
    points_url = f"{base_url}/points/{latitude},{longitude}"
    try:
        points_response = requests.get(points_url, headers=headers)
        points_response.raise_for_status()  # Raise an HTTPError for bad responses (4xx or 5xx)
        points_data = points_response.json()
    except requests.exceptions.RequestException as e:
        print(f"Error fetching NWS points data: {e}")
        return None

    forecast_grid_data_url = points_data.get("properties", {}).get("forecastGridData")
    if not forecast_grid_data_url:
        print("Could not find forecast grid data URL.")
        return None

    # Step 2: Get detailed forecast using the grid data URL
    try:
        forecast_response = requests.get(forecast_grid_data_url, headers=headers)
        forecast_response.raise_for_status()
        forecast_data = forecast_response.json()
        return forecast_data
    except requests.exceptions.RequestException as e:
        print(f"Error fetching NWS forecast data: {e}")
        return None

