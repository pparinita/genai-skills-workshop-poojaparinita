def geocode_place(place_name, api_key):
    """
    Converts a place name to its latitude and longitude coordinates using
    the Google Maps Geocoding API.

    Args:
        place_name (str): The name of the place to geocode (e.g., "Eiffel Tower").
        api_key (str): Your Google Cloud API key with Geocoding API enabled.

    Returns:
        dict or None: A dictionary containing 'lat' and 'lng' if successful,
                      otherwise None.
    """
    base_url = "https://maps.googleapis.com/maps/api/geocode/json"
    params = {
        "address": place_name,
        "key": api_key
    }

    try:
        response = requests.get(base_url, params=params)
        response.raise_for_status()  # Raise an exception for HTTP errors (4xx or 5xx)
        data = response.json()

        if data["status"] == "OK" and data["results"]:
            location = data["results"][0]["geometry"]["location"]
            return {"lat": location["lat"], "lng": location["lng"]}
        else:
            print(f"Geocoding failed for '{place_name}': {data.get('status', 'Unknown error')}")
            return None
    except requests.exceptions.RequestException as e:
        print(f"An error occurred during the API request: {e}")
        return None
    except json.JSONDecodeError:
        print(f"Failed to decode JSON response from API.")
        return None

